// ---------- pure helpers ----------

export const SOLID = new Set(["#", "B"]);

export function tileAt(room, tx, ty) {
  if (tx < 0 || ty < 0 || ty >= room.tiles.length || tx >= room.tiles[0].length) return "#";
  return room.tiles[ty][tx];
}

export function isSolid(room, tx, ty, broken = null) {
  const t = tileAt(room, tx, ty);
  if (t === "B" && broken && broken.has(tx + "," + ty)) return false;
  return SOLID.has(t);
}

export function isPlatform(room, tx, ty) {
  return tileAt(room, tx, ty) === "=";
}

export function isSpike(room, tx, ty) {
  return tileAt(room, tx, ty) === "^";
}

// Extract entity markers from a room's tiles.
export function parseMarkers(room) {
  const out = { enemies: [], saves: [], relic: null, npc: null, start: null };
  for (let y = 0; y < room.tiles.length; y++) {
    for (let x = 0; x < room.tiles[y].length; x++) {
      const c = room.tiles[y][x];
      if (c === "P") out.start = { x, y };
      else if (c === "S") out.saves.push({ x, y });
      else if (c === "R") out.relic = { x, y, id: room.relic };
      else if (c === "N") out.npc = { x, y, id: room.npc };
      else if (c === "E") out.enemies.push({ x, y, kind: "heckler" });
      else if (c === "F") out.enemies.push({ x, y, kind: "drunk" });
      else if (c === "T") out.enemies.push({ x, y, kind: "thief" });
    }
  }
  return out;
}

export function roomAt(gx, gy) {
  return ROOMS[gx + "," + gy] || null;
}

export function findStart() {
  for (const key of Object.keys(ROOMS)) {
    const m = parseMarkers(ROOMS[key]);
    if (m.start) {
      const [gx, gy] = key.split(",").map(Number);
      return { gx, gy, tx: m.start.x, ty: m.start.y };
    }
  }
  return null;
}
